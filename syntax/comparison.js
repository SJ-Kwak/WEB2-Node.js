console.log(1+1); // +: 이항연산자. 좌항과 우항을 처리하여 하나의 값으로 표현할 수 있게 하는 연산자
console.log(1===1); //true
console.log(1===2); //false
//비교연산자: 좌항과 우항의 값을 비교하여 true나 false 둘 중 하나의 값을 만들어 내는 연산자
//좌항과 우항이 정확하게 같은지 비교: ===
//=: 대입연산자. 변수에 값 대입. == or ===: 비교 연산자. ==쓰는 경우에는 ===로 쓰는 것 추천
console.log(1>2); //false
console.log(1<2); //true
